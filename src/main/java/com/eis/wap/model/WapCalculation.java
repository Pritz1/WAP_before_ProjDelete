package com.eis.wap.model;

public class WapCalculation {

	private Integer param_id;
	private String param_name;
	private Float weightage;
	private Float achieve;
	private Float score;
	private Float wap;
	private String rating;
	
	
	public Integer getParam_id() {
		return param_id;
	}
	public void setParam_id(Integer param_id) {
		this.param_id = param_id;
	}
	public String getParam_name() {
		return param_name;
	}
	public void setParam_name(String param_name) {
		this.param_name = param_name;
	}
	public Float getWeightage() {
		return weightage;
	}
	public void setWeightage(Float weightage) {
		this.weightage = weightage;
	}
	public Float getAchieve() {
		return achieve;
	}
	public void setAchieve(Float achieve) {
		this.achieve = achieve;
	}
	public Float getScore() {
		return score;
	}
	public void setScore(Float score) {
		this.score = score;
	}
	public Float getWap() {
		return wap;
	}
	public void setWap(Float wap) {
		this.wap = wap;
	}
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
    	
	
}
