public class TotalDocs {
	
	private String netid;
	private String ecode;
	private Integer totCntE;
	private Integer totCntF;
	private Integer totCntD;
	
	public String getNetid() {
		return netid;
	}
	public void setNetid(String netid) {
		this.netid = netid;
	}
	public String getEcode() {
		return ecode;
	}
	public void setEcode(String ecode) {
		this.ecode = ecode;
	}
	public Integer getTotCntE() {
		return totCntE;
	}
	public void setTotCntE(Integer totCntE) {
		this.totCntE = totCntE;
	}
	public Integer getTotCntF() {
		return totCntF;
	}
	public void setTotCntF(Integer totCntF) {
		this.totCntF = totCntF;
	}
	public Integer getTotCntD() {
		return totCntD;
	}
	public void setTotCntD(Integer totCntD) {
		this.totCntD = totCntD;
	}
	
}
